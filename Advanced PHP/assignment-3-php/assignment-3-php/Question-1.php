<!-- Inheritance, Polymorphism, and Abstraction -->


<?php
// (Encapsulation)

// Definition: Encapsulation is the concept of bundling data and methods that operate 
// on that data within one unit (class), and restricting access to some of the object's components.

// Purpose: It protects the internal state of an object from unintended or harmful interference.




// (Inheritance)

// Definition: Inheritance allows a class (child or subclass) to inherit attributes and methods 
// from another class (parent or superclass).

// Purpose: Promotes code reuse and establishes a hierarchical relationship between classes.



// (Polymorphism)

// Definition: Polymorphism allows objects of different classes to be treated as objects of a common superclass.

// Purpose: It enables the same interface to represent different types of objects.


// (Abstraction)

// Definition: Abstraction is the process of hiding the internal details and showing only essential features.

// Purpose: It simplifies complex systems by providing a clear and understandable interface.










































